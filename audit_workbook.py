def generate_audit_workbook(
    units: List,
    df: pd.DataFrame,
    output_path: Path,
) -> Path:
    """
    Generate the GIPS-aligned audit workbook and save it to output_path.

    Parameters
    ----------
    units       : list[DecisionUnit] from IQREAnalytics.aggregate_sequences()
    df          : cleaned source DataFrame (engine.df after aggregation,
                  original pandas index preserved for Data Lineage)
    output_path : destination path; parent directories created automatically

    Returns
    -------
    Resolved Path of the saved .xlsx file.

    Raises
    ------
    AuditWorkbookError
        If units is empty or df is missing required columns.
    """
    if not units:
        raise AuditWorkbookError(
            "[AUDIT FAIL] Cannot generate workbook: no Decision Units found."
        )

    required_df_cols = {
        "Trade #", "Type", "Date", "Price", "Size",
        "Position size (value)", "Profit", "Net P&L %",
        "Adverse excursion USDT", "Adverse excursion %",
        "Cumulative P&L USDT",
    }
    missing = required_df_cols - set(df.columns)
    if missing:
        raise AuditWorkbookError(
            f"[AUDIT FAIL] DataFrame missing columns: {sorted(missing)}"
        )

    output_path = Path(output_path).resolve()
    output_path.parent.mkdir(parents=True, exist_ok=True)

    utc_now = datetime.now(timezone.utc)
    utc_ts  = utc_now.strftime("%Y-%m-%d %H:%M:%S UTC")

    wb = Workbook()
    wb.remove(wb.active)          # discard default empty sheet

    # -- Excel metadata / document properties (Institutional Hardening) -------
    wb.properties.title          = "IQRE Institutional Audit Data — Production Build"
    wb.properties.subject        = "GIPS-Aligned Quantitative Reporting"
    wb.properties.creator        = "IQRE v1.5 -- Institutional Recalibration Build"
    wb.properties.lastModifiedBy = f"IQRE Audit Engine  |  {utc_ts}"
    wb.properties.description    = (
        f"Generated: {utc_ts}  |  "
        "GIPS 2026 Governance Protocol  |  "
        "Classification: Proprietary  |  "
        "Architect: Harol Antibar"
    )
    wb.properties.keywords = "GIPS; IQRE; DCA; PNE; MAE; institutional; production"
    wb.properties.category = "Institutional Audit"
    wb.properties.created  = utc_now

    _build_decision_units(wb, units)
    _build_traceability(wb, df)
    _build_legal_disclosure(wb, utc_ts)

    wb.save(str(output_path))
    return output_path


