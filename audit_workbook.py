def _build_legal_disclosure(wb: Workbook, utc_ts: str) -> None:
    """
    Sheet 3: Full legal disclaimer, limitation of liability, and governance
    disclosure.  Required by the Institutional Hardening specification.

    Layout
    ------
    - Column A : Text content (width 110, word-wrap enabled)
    - Column B : Generation timestamp metadata (narrow, right-aligned)
    """
    ws = wb.create_sheet("Legal_Disclosure")
    ws.sheet_properties.tabColor = _C_SILVER
    ws.column_dimensions["A"].width = 112
    ws.column_dimensions["B"].width = 32

    # Timestamp header row
    ts_cell = ws.cell(row=1, column=2, value=f"Generated (UTC): {utc_ts}")
    ts_cell.font      = Font(name="Calibri", size=9, italic=True, color="666666")
    ts_cell.alignment = Alignment(horizontal="right", vertical="center")

    xl_row = 2
    for text, style in _LEGAL_PARAGRAPHS:
        ws.row_dimensions[xl_row].height = 6 if style == "spacer" else None

        if style == "spacer":
            xl_row += 1
            continue

        c = ws.cell(row=xl_row, column=1, value=text)
        c.alignment = Alignment(
            horizontal="left",
            vertical="top",
            wrap_text=(style == "body"),
        )
        c.border = Border()  # no borders on this sheet

        if style == "title":
            c.font = Font(name="Calibri", bold=True, size=16, color=_C_NAVY)
            ws.row_dimensions[xl_row].height = 28
        elif style == "subtitle":
            c.font = Font(name="Calibri", size=12, italic=True, color="444444")
            ws.row_dimensions[xl_row].height = 20
        elif style == "heading":
            c.font = Font(name="Calibri", bold=True, size=13, color=_C_NAVY)
            ws.row_dimensions[xl_row].height = 22
            c.fill = _solid("DDE3EC")
        elif style == "subheading":
            c.font = Font(name="Calibri", bold=True, size=11, color=_C_NAVY)
            ws.row_dimensions[xl_row].height = 18
        elif style == "body":
            c.font = Font(name="Calibri", size=10, color=_C_DARK)
            ws.row_dimensions[xl_row].height = 52

        xl_row += 1

    # Footer stamp
    xl_row += 1
    stamp = ws.cell(
        row=xl_row, column=1,
        value=(
            f"IQRE v1.5 -- Institutional Recalibration Build  |  "
            f"GIPS 2026 Governance Protocol  |  "
            f"Audit Timestamp: {utc_ts}"
        ),
    )
    stamp.font      = Font(name="Calibri", size=8, italic=True, color="888888")
    stamp.alignment = Alignment(horizontal="left", vertical="center")
    ws.row_dimensions[xl_row].height = 16


