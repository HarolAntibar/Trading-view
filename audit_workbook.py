def _build_decision_units(wb: Workbook, units: List) -> None:
    """
    Sheet 1: one row per Decision Unit (DCA-aggregated trade sequence).

    Columns : Trade ID | Entries | Net P&L | PNE | MAE | MAE % | Outcome
    Extras  : totals / averages row, frozen header, auto-filter.
    """
    ws = wb.create_sheet("Decision_Units")
    ws.sheet_properties.tabColor = _C_NAVY
    ws.row_dimensions[1].height = 24

    # -- header --
    for col, (label, width, align, _) in enumerate(_DU_COLS, start=1):
        _write_header(ws, col, 1, label, align)
        ws.column_dimensions[get_column_letter(col)].width = width

    # -- data rows --
    n = len(units)
    for row_idx, u in enumerate(units):
        xl_row = row_idx + 2
        ws.row_dimensions[xl_row].height = 18
        bg = _row_fill(row_idx)
        o_color = _C_WIN if u.outcome == "WIN" else _C_LOSS

        row_cells = [
            # (value,            fmt,       h_align,   font_color, bold)
            (u.trade_id,         _FMT_INT,  "center",  _C_DARK,    False),
            (u.entries,          _FMT_INT,  "center",  _C_DARK,    False),
            (u.net_pnl,          _FMT_USD,  "right",   _C_DARK,    False),
            (u.pne,              _FMT_USD,  "right",   _C_DARK,    False),
            (u.mae,              _FMT_USD,  "right",   _C_DARK,    False),
            (u.mae_pct / 100.0,  _FMT_PCT,  "center",  _C_DARK,    False),
            (u.outcome,          None,      "center",  o_color,    True),
        ]
        for col, (val, fmt, h, fc, bold) in enumerate(row_cells, start=1):
            _write_cell(ws, xl_row, col, val, fmt, h, fc, bold, bg)

    # -- summary row --
    sr = n + 2                         # Excel row index for summary
    ws.row_dimensions[sr].height = 20
    sf = _solid(_C_TOTALS)
    last_data = n + 1                  # last data Excel row

    wins = sum(1 for u in units if u.outcome == "WIN")

    summary_cells = [
        # col  value/formula                         fmt        h_align  bold
        (1,  "TOTALS / AVG",                         None,      "center", True),
        (2,  f"=SUM(B2:B{last_data})",               _FMT_INT,  "center", True),
        (3,  f"=SUM(C2:C{last_data})",               _FMT_USD,  "right",  True),
        (4,  f"=AVERAGE(D2:D{last_data})",           _FMT_USD,  "right",  True),
        (5,  f"=MAX(E2:E{last_data})",               _FMT_USD,  "right",  True),
        (6,  f"=MAX(F2:F{last_data})",               _FMT_PCT,  "center", True),
        (7,  f"{wins} WIN / {n - wins} LOSS",        None,      "center", True),
    ]
    for col, val, fmt, h, bold in summary_cells:
        _write_cell(ws, sr, col, val, fmt, h, _C_NAVY, bold, sf)

    # -- freeze & filter --
    ws.freeze_panes = "A2"
    ws.auto_filter.ref = (
        f"A1:{get_column_letter(len(_DU_COLS))}{last_data}"
    )


def _build_traceability(wb: Workbook, df: pd.DataFrame) -> None:
    """
    Sheet 2: one row per cleaned source row, sorted by Trade # for easy
    cross-referencing with Sheet 1.

    The 'Source Row (Excel)' column contains the original row number
    (index + 2, accounting for the 1-indexed header) from the source
    tv_export_raw.xlsx 'List of trades' sheet, providing a verifiable
    audit trail per GIPS Data Lineage requirements.
    """
    ws = wb.create_sheet("Data_Traceability")
    ws.sheet_properties.tabColor = _C_GOLD
    ws.row_dimensions[1].height = 24

    # -- header --
    for col, (label, width, align, _) in enumerate(_TR_COLS, start=1):
        _write_header(ws, col, 1, label, align)
        ws.column_dimensions[get_column_letter(col)].width = width

    # -- sort display order: Trade # then by original source row --
    df_display = df.copy()
    df_display["_src_row"] = df_display.index + 2   # Excel row in source file
    df_display = df_display.sort_values(
        ["Trade #", "_src_row"], ascending=[True, True]
    ).reset_index(drop=True)

    # -- data rows --
    for row_idx, (_, row) in enumerate(df_display.iterrows()):
        xl_row = row_idx + 2
        ws.row_dimensions[xl_row].height = 18
        bg = _row_fill(row_idx)

        # Adverse excursion can be negative (adverse = below entry)
        adv_usdt    = row["Adverse excursion USDT"]
        adv_pct     = row["Adverse excursion %"] / 100.0
        net_pnl_pct = row["Net P&L %"] / 100.0

        row_cells = [
            # (value,                          fmt,        h_align)
            (int(row["_src_row"]),             _FMT_INT,   "center"),
            (int(row["Trade #"]),              _FMT_INT,   "center"),
            (row["Type"],                      None,       "left"),
            (row["Date"],                      _FMT_DATE,  "left"),
            (row["Price"],                     _FMT_USD4,  "right"),
            (row["Size"],                      _FMT_FLOAT, "right"),
            (row["Position size (value)"],     _FMT_USD,   "right"),
            (row["Profit"],                    _FMT_USD,   "right"),
            (net_pnl_pct,                      _FMT_PCT,   "center"),
            (adv_usdt,                         _FMT_USD,   "right"),
            (adv_pct,                          _FMT_PCT,   "center"),
            (row["Cumulative P&L USDT"],       _FMT_USD,   "right"),
        ]
        for col, (val, fmt, h) in enumerate(row_cells, start=1):
            _write_cell(ws, xl_row, col, val, fmt, h, fill=bg)

    n_rows = len(df_display)
    # -- freeze & filter --
    ws.freeze_panes = "A2"
    ws.auto_filter.ref = (
        f"A1:{get_column_letter(len(_TR_COLS))}{n_rows + 1}"
    )


