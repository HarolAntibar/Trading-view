# ---------------------------------------------------------------------------
# Number format strings (GIPS-standard representations)
# ---------------------------------------------------------------------------
_FMT_USD   = '$#,##0.00'           # monetary values  e.g. $1,969.22
_FMT_USD4  = '$#,##0.0000'         # high-precision prices  e.g. $1.0087
_FMT_INT   = '#,##0'               # integer counts
_FMT_FLOAT = '#,##0.000'           # non-monetary floats  e.g. 970.778
_FMT_PCT   = '0.00%'               # percentages stored as decimals (0.0291 → 2.91%)
_FMT_DATE  = 'YYYY-MM-DD HH:MM:SS' # ISO-8601 datetime


# ---------------------------------------------------------------------------
# Reusable style components
# ---------------------------------------------------------------------------
_THIN_SIDE = Side(border_style="thin", color="C8C8C8")
_BORDER    = Border(
    left=_THIN_SIDE, right=_THIN_SIDE,
    top=_THIN_SIDE,  bottom=_THIN_SIDE,
)


def _hdr_font() -> Font:
    return Font(name="Calibri", bold=True, color=_C_WHITE, size=11)


def _body_font(color: str = _C_DARK, bold: bool = False) -> Font:
    return Font(name="Calibri", size=10, color=color, bold=bold)


def _solid(color: str) -> PatternFill:
    return PatternFill("solid", fgColor=color)


def _align(h: str = "left", v: str = "center") -> Alignment:
    return Alignment(horizontal=h, vertical=v)


def _row_fill(data_row_idx: int) -> PatternFill:
    """Alternating fill: light-gray for even, white for odd (0-indexed)."""
    return _solid(_C_LGRAY if data_row_idx % 2 == 0 else _C_WHITE)


# ---------------------------------------------------------------------------
# Cell writers
# ---------------------------------------------------------------------------
def _write_header(
    ws,
    col: int,
    row: int,
    label: str,
    h_align: str = "center",
) -> None:
    """Write a single navy bold header cell."""
    c = ws.cell(row=row, column=col, value=label)
    c.font      = _hdr_font()
    c.fill      = _solid(_C_NAVY)
    c.alignment = _align(h_align)
    c.border    = _BORDER


def _write_cell(
    ws,
    row: int,
    col: int,
    value,
    fmt:         str | None  = None,
    h_align:     str          = "left",
    font_color:  str          = _C_DARK,
    bold:        bool         = False,
    fill:        PatternFill | None = None,
) -> None:
    """Write a single data cell with full formatting."""
    c = ws.cell(row=row, column=col, value=value)
    c.font      = _body_font(color=font_color, bold=bold)
    c.alignment = _align(h_align)
    c.border    = _BORDER
    if fill is not None:
        c.fill = fill
    if fmt is not None:
        c.number_format = fmt


